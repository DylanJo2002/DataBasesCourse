    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        try {
            int id = Integer.parseInt(textID.getText());
            int respuesta = JOptionPane.showConfirmDialog(null,
                    "¿Está seguro de eliminar a ".concat(txtNombre.getText()),
                     "Alerta", JOptionPane.YES_NO_OPTION);
            if (respuesta == JOptionPane.YES_OPTION) {
                try {
                    fachada.eliminarContacto(id);
                    limpiarCampos();
                    fachada.alertar("Contacto eliminado",
                            JOptionPane.INFORMATION_MESSAGE);
                    cargarEnTabla();
                    tabla.updateUI();
                } catch (Exception ex) {
                    fachada.alertar(ex.getMessage(),
                            JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (NumberFormatException ex) {
            fachada.alertar("Debe seleccionar un contacto",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void abstraerActualizacion(int tipo) {
        String[] entradas = new String[4];
        entradas[0] = txtNombre.getText().trim();
        entradas[1] = textApellido.getText().trim();
        entradas[2] = textEmail.getText().trim();
        entradas[3] = textTelefono.getText().trim();
        char genero = cbxGenero.getSelectedItem().toString().charAt(0);

        try {
            int edad = Integer.parseInt(textEdad.getText());

            if (validarInputs(entradas, edad)) {

                try {
                    Contacto contacto = new Contacto();
                    contacto.setNombre(entradas[0]);
                    contacto.setApellido(entradas[1]);
                    contacto.setEmail(entradas[2]);
                    contacto.setTelefono(entradas[3]);
                    contacto.setGenero(genero);
                    contacto.setEdad(edad);

                    if (tipo == 0) {
                        contacto.setId(Integer.parseInt(
                            textID.getText()));
                        fachada.editarContacto(contacto);
                        fachada.alertar("Contacto editado",
                                JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        fachada.crear(contacto);
                        fachada.alertar("Contacto creado",
                                JOptionPane.INFORMATION_MESSAGE);
                    }
                    cargarEnTabla();
                    tabla.updateUI();

                    limpiarCampos();

                } catch (Exception ex) {
                    fachada.alertar(ex.getMessage(), JOptionPane.ERROR_MESSAGE);
                }

                return;
            }
            throw new NumberFormatException("Entradas inválidas");
        } catch (NumberFormatException ex) {
            fachada.alertar(ex.getMessage(), JOptionPane.ERROR_MESSAGE);
        };

    }

    private void limpiarCampos() {
        textID.setText("");
        txtNombre.setText("");
        textApellido.setText("");
        textEdad.setText("");
        textTelefono.setText("");
        textEmail.setText("");
    }

