    private boolean validarInputs(String[] textInputs,
            int enteroEdad) {

        for (String textInput : textInputs) {
            if (textInput.isEmpty()) {
                return false;
            }
            if (textInputs[textInputs.length - 1] == textInput) {
                if (textInput.length() > 10) {
                    return false;
                }
            } else {
                if (textInput.length() > 100) {
                    return false;
                }
            }
        }
        if (enteroEdad < 0 || enteroEdad > 120) {
            return false;
        }

        return true;

    }

